package pe.pss.pointer.pointerbackend.model.dto;

public class CarreraEducativaDTO {
    private String idCarreraEducativa;
    private String glsCarreraEducativ;
    private String idInstitucion;

    public String getIdCarreraEducativa() {
        return idCarreraEducativa;
    }

    public void setIdCarreraEducativa(String idCarreraEducativa) {
        this.idCarreraEducativa = idCarreraEducativa;
    }

    public String getGlsCarreraEducativ() {
        return glsCarreraEducativ;
    }

    public void setGlsCarreraEducativ(String glsCarreraEducativ) {
        this.glsCarreraEducativ = glsCarreraEducativ;
    }

    public String getIdInstitucion() {
        return idInstitucion;
    }

    public void setIdInstitucion(String idInstitucion) {
        this.idInstitucion = idInstitucion;
    }
}
